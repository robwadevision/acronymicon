// ── Transform ─────────────────────────────────────────────────────────────

const dictPath = path.join(__dirname, "../src/acronyms.en.json");
const raw = fs.readFileSync(dictPath, "utf8");
const data = JSON.parse(raw);

const newData = {};

for (const [key, entry] of Object.entries(data)) {
  if (key === "_meta") {
    newData["_meta"] = {
      version: entry.version,
      lang: entry.lang,
      description: entry.description,
      schema: {
        definitions: "Array of definition objects — first is primary/most common",
        "definitions[].text": "The definition text",
        "definitions[].industry": "Optional: Tech | Finance | Sports | Medical"
      }
    };
    continue;
  }

  const allTexts = [entry.default, ...(entry.alternatives || [])];
  const definitions = allTexts.map((text) => {
    const industry = INDUSTRIES[text];
    return industry ? { text, industry } : { text };
  });

  newData[key] = { definitions };
}

const output = JSON.stringify(newData, null, 2) + "\n";
fs.writeFileSync(dictPath, output, "utf8");

// Verify: report any definition texts that were not in the lookup table
const missing = [];
for (const [key, entry] of Object.entries(newData)) {
  if (key === "_meta") continue;
  for (const def of entry.definitions) {
    if (!def.industry) {
      missing.push(`${key}: "${def.text}"`);
    }
  }
}

console.log(`Done. ${Object.keys(newData).length - 1} entries migrated.`);
if (missing.length > 0) {
  console.log(`\nUntagged definitions (${missing.length}) — no industry assigned:`);
  missing.forEach((m) => console.log(`  ${m}`));
}
